#include <stdio.h>

int main(void)
{
    char    un_caracter;
    int     un_entero;
    float   un_real;

    un_caracter = 'a';
    un_entero = 15;
    un_real = 27.12;
    printf("%c es el caracter. \n", un_caracter);
    printf("%d es el entero. \n", un_entero);
    printf("%f es el real. \n", un_real);
}